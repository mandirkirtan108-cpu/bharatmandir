const HI_PATTERNS = [
  [/^(\d+)\s+ch\s+·\s+([\d,]+)\s+verses$/i, '$1 अध्याय · $2 श्लोक'],
  [/^(\d+)%\s+read$/i, '$1% पढ़ा गया'],
  [/^Chapter\s+(\d+)$/i, 'अध्याय $1'],
  [/^CHAPTER\s+(\d+)$/i, 'अध्याय $1'],
  [/^Continue from Chapter\s+(\d+)\s*›?$/i, 'अध्याय $1 से जारी रखें ›'],
  [/^Ch\s+(\d+),\s+Verse\s+(\d+)$/i, 'अध्याय $1, श्लोक $2'],
  [/^(.+)\s+·\s+Ch\s+(\d+),\s+Verse\s+(\d+)$/i, '$1 · अध्याय $2, श्लोक $3'],
  [/^(\d+)\s+results for\s+"(.+)"$/i, '"$2" के लिए $1 परिणाम'],
  [/^No verses found for\s+"(.+)"$/i, '"$1" के लिए कोई श्लोक नहीं मिला'],
  [/^Search in\s+(.+)…$/i, '$1 में खोजें…'],
  [/^Opening\s+(.+)…$/i, '$1 खोला जा रहा है…'],
];

function normalize(text) {
  return String(text || '').replace(/\s+/g, ' ').trim();
}

export function translateStaticText(text, lang = 'en') {
  if (lang !== 'hi') return text;
  const leading = String(text).match(/^\s*/)?.[0] || '';
  const trailing = String(text).match(/\s*$/)?.[0] || '';
  const core = normalize(text);
  if (!core) return text;

  if (HI_EXACT[core]) return `${leading}${HI_EXACT[core]}${trailing}`;

  const plainCore = core.replace(/^[^\p{L}\p{N}]+/u, '').trim();
  if (HI_EXACT[plainCore]) {
    const prefix = core.slice(0, core.indexOf(plainCore));
    return `${leading}${prefix}${HI_EXACT[plainCore]}${trailing}`;
  }

  for (const [pattern, replacement] of HI_PATTERNS) {
    if (pattern.test(core)) return `${leading}${core.replace(pattern, replacement)}${trailing}`;
  }

  return text;
}

export function isTranslated(text, lang = 'en') {
  return translateStaticText(text, lang) !== text;
}
